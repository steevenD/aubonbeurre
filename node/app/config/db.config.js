const env = require('./env.js');
 
const Sequelize = require('sequelize');
const sequelize = new Sequelize(env.database, env.username, env.password, {
  host: env.host,
  dialect: env.dialect,
  operatorsAliases: false,
 
  pool: {
    max: env.max,
    min: env.pool.min,
    acquire: env.pool.acquire,
    idle: env.pool.idle
  }
});
 
const db = {};
 
db.Sequelize = Sequelize;
db.sequelize = sequelize;
 
db.automate = require('../model/automate.model.js')(sequelize, Sequelize);
db.donnee = require('../model/donnees.model.js')(sequelize, Sequelize);


db.automate.hasOne(db.donnee);

module.exports = db;